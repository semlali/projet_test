package org.miage.sid.dao;

public class Tuteur {
	
	
	private Integer id_tuteur;
	private String nom;
	private String prenom;
	private String sex;
	private String relation;
	private String adress;
	private String email;
	private String telmobile;
	private String telfix;
	private String ville;
	private String travail;
	
	public Tuteur(){
		
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getRelation() {
		return relation;
	}

	public void setRelation(String relation) {
		this.relation = relation;
	}

	public String getAdress() {
		return adress;
	}

	public void setAdress(String adress) {
		this.adress = adress;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelmobile() {
		return telmobile;
	}

	public void setTelmobile(String telmobile) {
		this.telmobile = telmobile;
	}

	public String getTelfix() {
		return telfix;
	}

	public void setTelfix(String telfix) {
		this.telfix = telfix;
	}

	public String getVille() {
		return ville;
	}

	public void setVille(String ville) {
		this.ville = ville;
	}

	public String getTravail() {
		return travail;
	}

	public void setTravail(String travail) {
		this.travail = travail;
	}

	public Integer getId_tuteur() {
		return id_tuteur;
	}

	public void setId_tuteur(Integer id_tuteur) {
		this.id_tuteur = id_tuteur;
	}
	
	
	

}
